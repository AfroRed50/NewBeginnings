/* ==== More/Less Button Function ==== */

// 2nd Paragraph
function buttonClick1() {
    var x = document.getElementById("shrink");
    if (x.style.display === "none") {
        x.style.display = "block";
    } else {
        x.style.display = "none";
    }
}

